import UIKit

public class ELColor: NSObject {
    /// iOS-Elements主要颜色是蓝色
    public static let primary = UIColor.rgb(64, 158, 255)

    /// 成功场景使用的颜色
    public static let success = UIColor.rgb(103, 194, 58)

    /// 警告场景使用的颜色
    public static let warning = UIColor.rgb(230, 162, 60)

    /// 危险色
    public static let danger = UIColor.rgb(245, 108, 108)

    /// 消息提示色
    public static let info = UIColor.rgb(144, 147, 153)
}
